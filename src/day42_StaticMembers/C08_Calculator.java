package day42_StaticMembers;

public class C08_Calculator {

    public static int toplama(int x, int y) {
        return x + y;
    }

    public static int cıkarma(int x, int y) {
        return x - y;
    }

    public static int carpma(int x, int y) {
        return x * y;
    }
    public static int bölme(int x,int y) {
        return x / y;
    }
}